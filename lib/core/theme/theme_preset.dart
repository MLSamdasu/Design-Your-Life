// C0.5: 테마 프리셋 열거형
// 6가지 시각적 테마 스타일을 정의한다.
// Hive settingsBox에 name 문자열로 저장/복원한다.

/// 앱 테마 프리셋 유형
/// 시각적 처리 방식(배경, 카드, 블러)만 다르며 ColorTokens의 MAIN/SUB는 변경하지 않는다
enum ThemePreset {
  /// 글래스모피즘 (기본): 반투명 유리 카드 + 그라디언트 배경
  glassmorphism,

  /// 미니멀 화이트: 깔끔한 플랫 디자인 + 미세 그림자 (블러 없음)
  minimal,

  /// 레트로 감성: 따뜻한 크림 톤 + 종이 질감 느낌 (블러 없음)
  retro,

  /// 네온 나이트: 어두운 배경 + 네온 글로우 효과 (약한 블러)
  neon,

  /// 깔끔한 테마: 미니멀리스트 + 화이트 스페이스 + 얇은 보더 + 클린 블루 악센트
  clean,

  /// 부드러운 테마: 따뜻한 파스텔 + 크림 배경 + 소프트 코랄 악센트 + 둥근 모서리
  soft,
}
